    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        request.setAttribute("roleList", RoleDAO.INSTANCE.getRoleList());
        request.getRequestDispatcher("adminView/AddNewUser.jsp").forward(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String company = request.getParameter("company");
        String username = request.getParameter("username");
        String password = request.getParameter("password");
        String email = request.getParameter("email");
        String fullName = request.getParameter("fullName");
        String gender = request.getParameter("gender");
        try {
            Date birthDate = Date.valueOf(request.getParameter("birthDate"));
            int roleId = Integer.parseInt(request.getParameter("roleId"));
            DAOAccount.INSTANCE.insertAccount(username, password, email, roleId);
            DAOAccount.INSTANCE.insertAccountProfile(DAOAccount.INSTANCE.login(username, password).getId(), fullName, birthDate, gender.equalsIgnoreCase("male"), "");
        } catch (NumberFormatException e) {
            doGet(request, response);
            return;
        }
        
        System.out.println(company);
    }

